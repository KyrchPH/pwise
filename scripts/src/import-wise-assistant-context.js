function normalizeName(value) {
  return String(value || '')
    .trim()
    .toLowerCase()
    .replace(/^[0-9]+[_\s-]*/, '')
    .replace(/[^a-z0-9]+/g, '');
}

function normalizeCell(value) {
  if (value == null) return null;
  const text = String(value).trim();
  return text === '' ? null : text;
}

function resolveWorkbookPath(arg) {
  if (!arg) return defaultWorkbookPath;
  return isAbsolute(arg) ? arg : resolve(process.cwd(), arg);
}

async function ensureWorkbookExists(filePath) {
  try {
    await access(filePath, fsConstants.F_OK);
  } catch {
    throw new Error(`Workbook not found: ${filePath}`);
  }
}

function buildConnectionString() {
  const explicit =
    process.env.SUPABASE_CONNECTION_STRING ||
    process.env.CONNECTION_STRING ||
    process.env.POSTGRES_URL ||
    process.env.POSTGRES_CONNECTION_STRING;

  const suppliedPassword = process.env.SUPABASE_DB_PASSWORD || process.env.DB_PASSWORD;

  if (explicit) {
    if (explicit.includes('[YOUR-PASSWORD]')) {
      if (!suppliedPassword) {
        throw new Error(
          'Supabase connection string still contains [YOUR-PASSWORD]. Set SUPABASE_DB_PASSWORD or SUPABASE_CONNECTION_STRING.',
        );
      }
      return explicit.replace('[YOUR-PASSWORD]', encodeURIComponent(suppliedPassword));
    }
    return explicit;
  }

  const host = process.env.DB_HOST;
  const port = process.env.DB_PORT || '6543';
  const database = process.env.DATABASE || process.env.PGDATABASE || 'postgres';
  const user = process.env.DB_USER || process.env.PGUSER;

  if (!host || !user || !suppliedPassword) {
    throw new Error(
      'Missing Supabase Postgres credentials. Set SUPABASE_CONNECTION_STRING or provide DB_HOST, DB_PORT, DATABASE, DB_USER, and SUPABASE_DB_PASSWORD.',
    );
  }

  return `postgresql://${encodeURIComponent(user)}:${encodeURIComponent(suppliedPassword)}@${host}:${port}/${database}`;
}

function buildClient() {
  const connectionString = buildConnectionString();
  const shouldUseSsl =
    !process.env.SUPABASE_DISABLE_SSL &&
    !/localhost|127\.0\.0\.1/i.test(connectionString) &&
    !/sslmode=disable/i.test(connectionString);

  return new Client({
    connectionString,
    ssl: shouldUseSsl ? { rejectUnauthorized: false } : undefined,
  });
}

function findSheetName(workbook, aliases) {
  const wanted = new Set(aliases.map(normalizeName));
  return workbook.SheetNames.find((name) => wanted.has(normalizeName(name))) || null;
}

function readSheet(workbook, config) {
  const sheetName = findSheetName(workbook, config.aliases);
  if (!sheetName) {
    throw new Error(`Missing worksheet for ${config.label}. Expected one of: ${config.aliases.join(', ')}`);
  }

  const worksheet = workbook.Sheets[sheetName];
  const rows = XLSX.utils.sheet_to_json(worksheet, {
    header: 1,
    defval: null,
    raw: false,
    blankrows: false,
  });

  if (rows.length === 0) return { sheetName, rows: [] };

  const headers = rows[0];
  const headerIndex = new Map();
  headers.forEach((value, index) => {
    const normalized = normalizeName(value);
    if (normalized) headerIndex.set(normalized, index);
  });

  for (const [header] of config.columns) {
    if (!headerIndex.has(normalizeName(header))) {
      throw new Error(`Worksheet "${sheetName}" is missing required column "${header}".`);
    }
  }

  const dataRows = rows.slice(1).filter((row) => row.some((cell) => normalizeCell(cell) !== null));
  const mappedRows = dataRows.map((row) =>
    config.columns.map(([header]) => normalizeCell(row[headerIndex.get(normalizeName(header))])),
  );

  return { sheetName, rows: mappedRows };
}

function schemaSql(sql) {
  return sql.replaceAll('%SCHEMA%', defaultSchema);
}

function tableRef(name) {
  return `"${defaultSchema}"."${name}"`;
}

async function createTables(client) {
  for (const config of TABLES) {
    await client.query(schemaSql(config.ddl));
  }
}

async function replaceRows(client, config, rows) {
  await client.query(`TRUNCATE TABLE ${tableRef(config.table)} RESTART IDENTITY`);
  if (rows.length === 0) return;

  const dbColumns = ['sort_order', ...config.columns.map(([, db]) => db)];
  const values = [];
  const tuples = rows.map((row, rowIndex) => {
    const parts = dbColumns.map((_, columnIndex) => `$${rowIndex * dbColumns.length + columnIndex + 1}`);
    values.push(rowIndex + 1, ...row);
    return `(${parts.join(', ')})`;
  });

  await client.query(
    `INSERT INTO ${tableRef(config.table)} (${dbColumns.map((name) => `"${name}"`).join(', ')}) VALUES ${tuples.join(', ')}`,
    values,
  );
}

async function main() {
  const workbookPath = resolveWorkbookPath(process.argv[2]);
  await ensureWorkbookExists(workbookPath);

  const workbook = XLSX.readFile(workbookPath, { cellDates: false });
  const parsed = TABLES.map((config) => ({ config, ...readSheet(workbook, config) }));

  const client = buildClient();
  await client.connect();

  try {
    await client.query('BEGIN');
    await createTables(client);
    for (const entry of parsed) {
      await replaceRows(client, entry.config, entry.rows);
    }
    await client.query('COMMIT');
  } catch (error) {
    await client.query('ROLLBACK').catch(() => {});
    throw error;
  } finally {
    await client.end();
  }

  console.log(`Imported assistant context into Supabase from ${workbookPath}`);
  for (const { config, sheetName, rows } of parsed) {
    console.log(`- ${defaultSchema}.${config.table}: ${rows.length} rows from "${sheetName}"`);
  }
}

main().catch((error) => {
  console.error('[assistant-context-import] failed:', error.message);
  process.exit(1);
});
