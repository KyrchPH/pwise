-- auth_otp_codes — generic one-time email codes for sensitive auth actions
-- (purpose='login' for the new-device login OTP, 'email_change' for changing the
-- account email). One pending code per (user_id, purpose); a fresh request replaces
-- the previous one. Mirrors password_change_codes (bcrypt-hashed code, TTL, attempt
-- cap). payload carries action data (e.g. the pending new email for 'email_change').
CREATE TABLE IF NOT EXISTS auth_otp_codes (
  user_id    INT NOT NULL,
  purpose    VARCHAR(20) NOT NULL,
  code_hash  VARCHAR(255) NOT NULL,
  expires_at DATETIME NOT NULL,
  verified   BOOLEAN NOT NULL DEFAULT FALSE,
  attempts   INT NOT NULL DEFAULT 0,
  payload    JSON NULL,
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (user_id, purpose),
  CONSTRAINT fk_otp_user FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- trusted_devices — a browser the user chose to trust ("Trust this device"), so
-- future logins from it skip the email OTP. The server issues a high-entropy secret
-- and stores only its SHA-256 (token_hash); the raw secret lives in the client's
-- localStorage. A trusted device only bypasses the OTP — it NEVER bypasses the
-- password — so a stolen token alone can't log in. Sliding 30-day expiry; revoked
-- on "log out all other devices" and on password change.
CREATE TABLE IF NOT EXISTS trusted_devices (
  id           INT AUTO_INCREMENT PRIMARY KEY,
  user_id      INT NOT NULL,
  token_hash   CHAR(64) NOT NULL,
  label        VARCHAR(255) NULL,
  ip           VARCHAR(64) NULL,
  created_at   DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  last_used_at DATETIME NULL,
  expires_at   DATETIME NOT NULL,
  revoked_at   DATETIME NULL,
  UNIQUE KEY uq_trusted_devices_token (token_hash),
  CONSTRAINT fk_trusted_devices_user FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE,
  INDEX idx_trusted_devices_user (user_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- login_history — login sessions: one row per successful login = a revocable
-- "session". The JWT carries this row's id (sid); requireAuth rejects a token whose
-- session row is missing or has revoked_at set (this is what powers "log out of this
-- device" / "all other devices"). last_seen_at is bumped (throttled) on activity, and
-- the same rows back the Profile → Security list. Deleting a user drops their sessions.
CREATE TABLE IF NOT EXISTS login_history (
  id           INT AUTO_INCREMENT PRIMARY KEY,
  user_id      INT NOT NULL,
  ip           VARCHAR(64) NULL,
  user_agent   VARCHAR(512) NULL,
  created_at   DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  last_seen_at DATETIME NULL,
  revoked_at   DATETIME NULL,
  CONSTRAINT fk_login_history_user FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE,
  INDEX idx_login_history_user (user_id, created_at)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- post_pool — uploaded post content (the pool the agent draws from) ------
CREATE TABLE IF NOT EXISTS post_pool (
  id              INT AUTO_INCREMENT PRIMARY KEY,
  user_id         INT NOT NULL,
  caption         TEXT,
  media_type      VARCHAR(50),
  post_kind       VARCHAR(20) NOT NULL DEFAULT 'post',  -- 'post' (text/photo) | 'video' (feed video) | 'reel'; orthogonal to media_type
  media_url       TEXT,
  s3_key          TEXT,
  thumbnail_s3_key TEXT,                       -- optimized still (first video frame / downscaled image) for previews
  target_platform VARCHAR(100),
  account_id      INT NULL,                    -- which connected page this post publishes to
  status          VARCHAR(50) NOT NULL DEFAULT 'draft',
  priority        INT NOT NULL DEFAULT 0,
  scheduled_at    DATETIME NULL,
  posted_at       DATETIME NULL,
  failed_reason   TEXT,
  -- Engagement pulled back from the platform by the n8n sync flow:
  platform_post_id     VARCHAR(255) NULL,   -- published post's ID on the platform (e.g. {page}_{post})
  reactions_count      INT NULL,
  comments_count       INT NULL,
  shares_count         INT NULL,
  views_count          INT NULL,            -- video/reel views (NULL for image/text)
  video_watch_time_s   INT NULL,            -- total watch time across all views, in seconds (video only)
  video_avg_watch_s    INT NULL,            -- average time watched per view, in seconds (video only)
  engagement_synced_at DATETIME NULL,       -- last time engagement was refreshed
  created_at      DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at      DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  CONSTRAINT fk_post_pool_user FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE,
  CONSTRAINT fk_post_pool_account FOREIGN KEY (account_id) REFERENCES platform_accounts(id) ON DELETE SET NULL,
  CONSTRAINT chk_post_pool_status
    CHECK (status IN ('draft','ready','posting','posted','failed','archived','expired','deleted')),
  CONSTRAINT chk_post_pool_media_type
    CHECK (media_type IS NULL OR media_type IN ('image','video')),
  -- post_kind picks the publish path: 'post' → /feed or /photos, 'video' → /videos, 'reel' → /video_reels.
  CONSTRAINT chk_post_pool_kind
    CHECK (post_kind IN ('post','video','reel')),
  -- Supports the scheduler claim query:
  --   WHERE user_id = ? AND status = 'ready'
  --   ORDER BY priority DESC, created_at ASC  ... FOR UPDATE SKIP LOCKED LIMIT 1
  INDEX idx_post_pool_claim (user_id, status, priority DESC, created_at),
  INDEX idx_post_pool_account (account_id, status)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

