-- =====================================================================
-- Auto Post Agent — database schema (MySQL 8.0+)
-- Idempotent: safe to run multiple times. Apply with `npm run db:migrate`.
--
-- Notes / deviations from the original plan:
--   * Engine InnoDB, charset utf8mb4 — captions contain emojis, and utf8mb4
--     is required to store 4-byte characters.
--   * Timestamps are DATETIME storing UTC (the app converts to each user's
--     `timezone`). created_at/updated_at use DEFAULT CURRENT_TIMESTAMP +
--     ON UPDATE CURRENT_TIMESTAMP, so MySQL maintains updated_at natively
--     (no trigger needed).
--   * status / media_type use CHECK constraints (enforced on MySQL 8.0.16+).
--   * Foreign keys are table-level — MySQL silently ignores inline column
--     REFERENCES, so they must be declared as constraints.
--   * posting_settings.user_id is UNIQUE (one settings row per user); the
--     post_pool index matches the scheduler's claim query.
-- =====================================================================

-- access_roles — named module-access presets ("User Roles"). Defined before
-- users/invites because both carry an FK to it. `created_by` is audit-only (no FK)
-- so this table depends on nothing and can lead the file. Named access_roles (not
-- `roles`) to avoid confusion with the users.role auth column.
CREATE TABLE IF NOT EXISTS access_roles (
  id            INT AUTO_INCREMENT PRIMARY KEY,
  name          VARCHAR(80) NOT NULL,
  module_access JSON NOT NULL,
  created_by    INT NULL,
  created_at    DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at    DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  UNIQUE KEY uq_access_roles_name (name)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- users — account information -------------------------------------------
CREATE TABLE IF NOT EXISTS users (
  id            INT AUTO_INCREMENT PRIMARY KEY,
  name          VARCHAR(255) NOT NULL,
  email         VARCHAR(255) NOT NULL,
  password_hash TEXT NOT NULL,
  role          VARCHAR(20) NOT NULL DEFAULT 'user',
  module_access JSON NULL,
  role_id       INT NULL,                     -- live link to an access_roles preset (NULL = Custom)
  avatar_s3_key TEXT NULL,
  is_active     BOOLEAN NOT NULL DEFAULT TRUE,
  -- Brute-force lockout: incremented on each wrong password, reset on success.
  -- When it reaches the limit, locked_until is set (UTC) and new logins are refused
  -- until it passes (lazy auto-unlock) or an admin clears it from Accounts.
  failed_login_attempts INT NOT NULL DEFAULT 0,
  locked_until  DATETIME NULL,
  deleted_at    DATETIME NULL,
  created_at    DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at    DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  UNIQUE KEY uq_users_email (email),
  CONSTRAINT chk_users_role CHECK (role IN ('super_admin', 'admin', 'user')),
  CONSTRAINT fk_users_access_role FOREIGN KEY (role_id) REFERENCES access_roles(id) ON DELETE SET NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

