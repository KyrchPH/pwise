-- platform_accounts — connected posting channels (Facebook pages) -------
-- Sensitive credentials (app_secret, app_client_token, access_token) MUST be
-- encrypted before being written here — see server/src/utils/crypto.util.js.
-- The page list is shared/global; user_id records the admin creator (audit).
CREATE TABLE IF NOT EXISTS platform_accounts (
  id               INT AUTO_INCREMENT PRIMARY KEY,
  user_id          INT NOT NULL,
  platform_name    VARCHAR(100) NOT NULL,
  account_name     VARCHAR(255),                -- page display name
  fb_page_id       VARCHAR(255),                -- Facebook Page ID
  app_id           VARCHAR(255),                -- Facebook App ID (not secret)
  app_secret       TEXT,                        -- ENCRYPTED
  app_client_token TEXT,                        -- ENCRYPTED
  access_token     TEXT,                        -- ENCRYPTED page access token
  telegram_bot_name     VARCHAR(255),           -- optional Telegram bot attached to this page
  telegram_bot_token    TEXT,                   -- ENCRYPTED Telegram bot API key
  telegram_bot_username VARCHAR(255),           -- Telegram bot @username (from getMe)
  -- Optional Instagram + WhatsApp channels on this page (migration 040). IG reuses the
  -- page access_token; WhatsApp has its own encrypted token + phone number id.
  instagram_account_id   VARCHAR(64)  NULL,      -- IG professional account id
  instagram_username     VARCHAR(255) NULL,
  wa_phone_number_id     VARCHAR(64)  NULL,      -- WhatsApp Cloud API phone number id
  wa_business_account_id VARCHAR(64)  NULL,      -- WABA id
  wa_phone_display       VARCHAR(40)  NULL,      -- human number for display
  wa_access_token        TEXT         NULL,      -- ENCRYPTED WhatsApp system-user token
  -- Per-page "Business profile" (migration 042) — admin-filled contact / location /
  -- hours, surfaced to the AI agent via its get_page_info tool. One JSON object; every
  -- field optional. NULL = no profile yet.
  business_profile JSON NULL,
  -- Customer-survey settings (migration 058) — { enabled, chancePct, cooldownDays }.
  -- NULL = defaults (disabled). Admin-edited in Settings → Facebook Pages.
  survey_config    JSON NULL,
  -- Per-page survey email sender (migration 061). The app password is ENCRYPTED.
  survey_sender_email VARCHAR(255) NULL,
  survey_sender_name VARCHAR(255) NULL,
  survey_sender_app_password TEXT NULL,
  refresh_token    TEXT,                        -- ENCRYPTED (reserved)
  token_expires_at DATETIME NULL,
  is_active        BOOLEAN NOT NULL DEFAULT TRUE,
  created_at       DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
  updated_at       DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  CONSTRAINT fk_platform_accounts_user FOREIGN KEY (user_id) REFERENCES users(id) ON DELETE CASCADE,
  INDEX idx_platform_accounts_user (user_id, platform_name),
  INDEX idx_pa_instagram (instagram_account_id),
  INDEX idx_pa_whatsapp (wa_phone_number_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

